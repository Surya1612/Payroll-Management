        File f=chooser.getSelectedFile();

        filename=f.getAbsolutePath();
        ImageIcon Icon=new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(img1.getWidth(),img1.getHeight(),Image.SCALE_DEFAULT));
