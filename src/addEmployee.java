                txtcont.setText(add6);

                String add7 =rs.getString("address1");
                txtadd1.setText(add7);

                String add8 =rs.getString("department");
                txtdept.setText(add8);
                
                String add10 =rs.getString("salary");
                txtsal.setText(add10);

                String add11 =rs.getString("address2");
                txtadd2.setText(add11);
                
                String add12 =rs.getString("apartment");
                txthouse.setText(add12);
                
                String add13 =rs.getString("postcode");
                txtpost.setText(add13);
                
                String add14 =rs.getString("status");
                txtstatus.setText(add14);
                
                String add15 =rs.getString("date_hired");
                txtdate.setText(add15);
                
                String add16 =rs.getString("job_title");
                txtjob.setText(add16);
                
                String add17 =rs.getString("designation");
                txtdes.setText(add17);
          
                String add18=rs.getString("gender");
                if(add18.equals("male")){
                    txtmale.setSelected(true);
                }else{
                    txtfemale.setSelected(true);
                }
                
                
                byte[] image = rs.getBytes("Image");
                ImageIcon imageIcon = new ImageIcon(new ImageIcon(image).getImage().getScaledInstance(img.getY(), img.getX(), Image.SCALE_SMOOTH));
                img1.setIcon(imageIcon);
                 
